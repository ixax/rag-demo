"""MCP server exposing retrieval-augmented search/answering over the Qdrant
collection populated by ../ingest (services/ingest).

Pipeline for both tools:
  1. embed the query with MODEL_EMBED (same model used at ingest time,
     so query and document vectors live in the same space)
  2. fetch TOP_K_RETRIEVE nearest chunks from Qdrant
  3. rerank those candidates via the standalone reranker HTTP service (see
     /services/reranker) and keep the top TOP_K_RERANK -- skippable via
     config.yml's search_tools.reranker.enabled
  4. `answer_question` additionally feeds the reranked chunks as context to a
     generation backend and returns its generated answer. The backend is
     selected by config.yml's search_tools.backend.type:
       - "" : generation is skipped -- answer_question returns retrieved
         context only, same as search_documents.
       - "ollama" (default): the local MODEL_INSTRUCT_INTERNAL via Ollama's
         /api/chat.
       - "anthropic_token": the Claude Messages API (MODEL_INSTRUCT_EXTERNAL,
         e.g. claude-sonnet-5) via the official `anthropic` SDK, using
         ANTHROPIC_API_KEY -- billed per token (see libs/anthropic.py).
       - "anthropic_subscription": the same model, but generated by shelling
         out to the headless `claude` CLI (Claude Code, non-interactive
         mode) using CLAUDE_CODE_OAUTH_TOKEN -- billed against a Claude
         subscription instead of a token balance. This is a genuinely
         different mechanism from anthropic_token, not just a different
         credential on the same SDK call -- see libs/claude_cli.py's
         docstring for why the Messages API itself has no subscription-
         billed mode.
     Thinking is left off for anthropic_token (system prompt already asks
     for a direct, ungrounded-guess-free answer) so latency stays comparable
     to the Ollama path; anthropic_subscription's CLI manages that itself.

A third, independent tool -- `anthropic_chat` -- sends raw text straight to
Claude with no retrieval/RAG context at all, for ad-hoc use outside the
knowledge-base pipeline. It's gated by config.yml's anthropic_chat.enabled
and has its own model/max_tokens/auth (token or subscription), separate from
search_tools.backend.type above -- the two can point at different Claude models and
different credentials, since they're unrelated use cases that just happen to
share the same build_client()/generate() functions in libs/anthropic.py.

Both search_documents/answer_question return structured JSON rather than
plain text: the retrieved chunks (the local data the reasoning is grounded in), the reasoning itself
(`answer_question` only -- see config.yml's search_tools.system_prompt for the Reasoning/
Answer/Sources format, which every generation backend follows since it's
shared), and a `trace` of pipeline steps with wall-clock timings. This is
meant to be consumed by a thin presentation layer (e.g. a Haiku-model agent)
rather than reasoned about further -- the reasoning already happened here.

Exposed over streamable-http so it can be added as a remote MCP connector in
both Open WebUI and Claude (Console / Desktop / claude.ai).

This file is handlers + wiring only. Everything else -- config schema,
retrieval, timing, answer parsing, and the Ollama/Anthropic backend calls --
lives in libs/. Env vars and config.yml are read *only* here; every libs/
function takes what it needs as arguments instead of reading globals itself.
"""

from __future__ import annotations

import functools
from pathlib import Path

from environs import Env
from mcp.server.fastmcp import FastMCP
from opentelemetry.trace import format_trace_id
from qdrant_client import QdrantClient

from _common.logging_config import configure_logging, get_logger
from _common.ollama import build_client, embed_query
from _common.tracing import OTEL_EXPORTER_OTLP_ENDPOINT, configure_tracing, get_meter, get_tracer

from .libs import anthropic as anthropic_lib
from .libs import claude_cli as claude_cli_lib
from .libs import ollama as ollama_lib
from .libs.config import MCPServerConfig, load_config, parse_structured_answer
